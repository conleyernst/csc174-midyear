              <p class="card-text">Potatoes contain toxic compounds known as glycoalkaloids, of which the most prevalent are solanine and chaconine. Solanine is also found in other plants in the family Solanaceae, which includes such plants as the deadly nightshade (Atropa belladonna), henbane (Hyoscyamus niger) and tobacco (Nicotiana), as well as eggplant and tomato. These compounds, which protect the plant from its predators, are, in general, concentrated in its leaves, stems, sprouts, and fruits (in contrast to the roots). In a summary of several studies, the glycoalkaloid content was highest in flowers and sprouts and lowest in the tuber flesh (in order from highest to lowest content, generally: flowers, sprouts, leaves, skin, roots, berries, peel [skin plus outer cortex of tuber flesh], stems, and tuber flesh).</p>

              <p class="card-text">Exposure to light, physical damage, and age increase glycoalkaloid content within the tuber. Cooking at high temperatures—over 170 °C (338 °F)—partly destroys these compounds. The concentration of glycoalkaloid in wild potatoes suffices to produce toxic effects in humans. Glycoalkaloids may cause headaches, diarrhea, cramps, and in severe cases coma and death; however, poisoning from potatoes occurs very rarely. Light exposure causes greening from chlorophyll synthesis, thus giving a visual clue as to areas of the tuber that may have become more toxic; however, this does not provide a definitive guide, as greening and glycoalkaloid accumulation can occur independently of each other. Varieties contain different levels of glycoalkaloids. The Lenape variety was released in 1967 but was withdrawn in 1970 as it contained high levels of glycoalkaloids. Since then breeders developing new varieties test for this, and sometimes have to discard an otherwise promising cultivar.</p>

              <p class="card-text">Breeders try to keep glycoalkaloid levels below 200 mg/kg (200 ppmw). However, when these commercial varieties turn green, they can still approach concentrations of solanine of 1000 mg/kg (1000 ppmw). In normal potatoes, analysis has shown solanine levels may be as little as 3.5% of the breeders' maximum, with 7–187 mg/kg being found. While a normal potato has 12–20 mg/kg of glycoalkaloid content, a green tuber contains 250–280 mg/kg, and green skin 1500–2200 mg/kg.</p>
